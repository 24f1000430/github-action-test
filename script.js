// Counter animation for homelab stats
function animateCounter(element, target, duration = 2000) {
    let start = 0;
    const increment = target / (duration / 16);
    
    function updateCounter() {
        start += increment;
        if (start < target) {
            element.textContent = Math.floor(start) + '+';
            requestAnimationFrame(updateCounter);
        } else {
            element.textContent = target + '+';
        }
    }
    
    updateCounter();
}

// Animate counters when homelab section comes into view
const homelabObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const statNumbers = entry.target.querySelectorAll('.stat-number');
            statNumbers.forEach(stat => {
                const text = stat.textContent;
                if (text.includes('+')) {
                    const number = parseInt(text);
                    animateCounter(stat, number);
                } else if (text.includes('%')) {
                    const number = parseFloat(text);
                    animateCounter(stat, number, 1500);
                }
            });
            homelabObserver.unobserve(entry.target);
        }
    });
}, { threshold: 0.5 });

const homelabSection = document.querySelector('.homelab');
if (homelabSection) {
    homelabObserver.observe(homelabSection);
}

